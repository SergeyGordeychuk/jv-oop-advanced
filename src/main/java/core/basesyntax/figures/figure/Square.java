package core.basesyntax.figures.figure;

import core.basesyntax.figures.Squares;

public class Square extends Squares {
    public Square(String color, int side) {
        super(color, side);
    }

    @Override
    public void draw() {
        super.draw();
    }

    @Override
    public double getArea() {
        return super.getArea();
    }

    @Override
    public int getSide() {
        return super.getSide();
    }

    @Override
    public void setSide(int side) {
        super.setSide(side);
    }
}
